#include "ptp_packets_example.h"


// --- DATOS ENVIO MINIMAC2 ---
uint8_t packet_prueba_100[104] = {
    // --- PREAMBULO (8 bytes) ---
    0x55, 0x55, 0x55, 0x55, 0x55, 0x55, 0x55, 0xD5,

    // --- CABECERA ETHERNET (14 bytes) ---
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, // MAC Destino (Broadcast)
    0x02, 0x00, 0x00, 0x00, 0x00, 0x01, // MAC Origen
    0x88, 0xF7,                         // EtherType PTP (1588)

    // --- CABECERA PTP (34 bytes) ---
    0xFF, 0x02,                                     // MessageType FF (prueba), Version (2)
    0x00, 0x2C,                                     // Message Length (44 bytes PTP)
    0x00, 0x00,                                     // Domain, Reserved
    0x02, 0x00,                                     // Flags (Two-Step)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // Correction Field
    0x00, 0x00, 0x00, 0x00,                         // Reserved
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // Clock ID (Source Port)
    0x00, 0x01,                                     // Port ID
    0x00, 0x50,                                     // Sequence ID (0x50 = 80)
    0x00, 0x00,                                     // Control Field, LogMessageInterval

    // --- PAYLOAD PTP (10 bytes Origin Timestamp para Sync) ---
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,

    // --- PADDING MASIVO (38 bytes para llegar a 104) ---
    // Hasta la línea anterior llevábamos 66 bytes. 
    // Añadimos 38 ceros para tener margen de sobra para tus pruebas.
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00
};



uint8_t payload_ptp_sync[44] = {
    // --- CABECERA PTP (34 bytes) ---
    0x00,                                           // [0] MessageType (0x00 = Sync)
    0x02,                                           // [1] Version PTP (v2)
    0x00, 0x2C,                                     // [2-3] Message Length (44 bytes)
    0x00, 0x00,                                     // [4-5] Domain, Reserved
    0x02, 0x00,                                     // [6-7] Flags (Two-Step)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // [8-15] Correction Field
    0x00, 0x00, 0x00, 0x00,                         // [16-19] Reserved
    0x02, 0x00, 0x00, 0xFF, 0xFE, 0x00, 0x00, 0x01, // [20-27] Clock ID
    0x00, 0x01,                                     // [28-29] Port ID
    0x00, 0x00,                                     // [30-31] Sequence ID (Lo cambiaremos al vuelo)
    0x00, 0x7F,                                     // [32-33] Control Field, LogMessageInterval

    // --- PAYLOAD PTP (10 bytes Origin Timestamp) ---
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 
};